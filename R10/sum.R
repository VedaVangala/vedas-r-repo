#' Add two numbers together
#'
#' This function takes two arguments, `y` and `z`, and returns their sum.
#'
#' @param y a numeric value to be added
#' @param z a numeric value to be added
#'
#' @return the sum of `y` and `z`
#'
#' @examples
#' s(2, 3)
#'
#' @export
s = function(y, z) {
  result = y + z
  return(result)
}
